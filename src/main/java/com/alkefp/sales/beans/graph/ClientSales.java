package com.alkefp.sales.beans.graph;

public class ClientSales {

	private String fyyear;
	private String clientName;
	private Double billAmt;
	private Double partPayAmt;
	private Double remainingPayAmt;
	
	public String getFyyear() {
		return fyyear;
	}
	public void setFyyear(String fyyear) {
		this.fyyear = fyyear;
	}
	public String getClientName() {
		return clientName;
	}
	public void setClientName(String clientName) {
		this.clientName = clientName;
	}
	public Double getBillAmt() {
		return billAmt;
	}
	public void setBillAmt(Double billAmt) {
		this.billAmt = billAmt;
	}
	public Double getPartPayAmt() {
		return partPayAmt;
	}
	public void setPartPayAmt(Double partPayAmt) {
		this.partPayAmt = partPayAmt;
	}
	public Double getRemainingPayAmt() {
		return remainingPayAmt;
	}
	public void setRemainingPayAmt(Double remainingPayAmt) {
		this.remainingPayAmt = remainingPayAmt;
	}
	

}
